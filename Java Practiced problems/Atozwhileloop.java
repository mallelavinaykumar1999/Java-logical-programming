/******************************************************************************
Write a JAVAprogram to print all alphabets from a to z. - using while loop
*******************************************************************************/
import java.util.*;
public class Atozwhileloop
{
	public static void main(String[] args) {
	    Scanner sc=new Scanner(System.in);
	    char ch='a';
	    while(ch>=91 && ch<=122){
	        System.out.print(ch+" ");
	        ch++;
	    }
	    
	    
		
	}
}
